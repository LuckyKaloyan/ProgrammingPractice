package SoftUniJava.EntryModule.NestedLoops.NestedLoopsLab;

public class Multiplication {
    public static void main(String[] args) {


        for(int i =1; i<=10; i++){
            for(int k = 1; k<=10; k++){
                System.out.println(i+" * "+k+" = "+(k*i));
            }
        }
    }
}
